package logica;
/**
 * Clase que representa una moneda con un valor de 1000;
 * Hereda de la clase abstracta {@link Moneda}.
 *
 * @author Antonia-FSR
 */
public class Moneda1000 extends Moneda {
    public Moneda1000(){
        super();
    }
    public int getValor(){
        return 1000;
    }
}
